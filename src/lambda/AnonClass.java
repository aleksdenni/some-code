package lambda;

import java.util.function.Consumer;

public class AnonClass {
    private int age;
    private String name;

    public AnonClass(int age, String name) {
        this.age = age;
        this.name = name;
    }

    public int getAge() {
        return age;
    }


    public String getName() {
        return name;
    }

}
